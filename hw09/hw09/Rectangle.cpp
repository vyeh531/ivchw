// Rectangle.cpp

#include "Rectangle.h"

Rectangle::Rectangle()
{
	length = 0.;
	width = 0.;
}
//=================================================================================
//
//=================================================================================
Rectangle::~Rectangle() {}
//=================================================================================
//
//=================================================================================
double Rectangle::calcPerimeter()
{
	return (double(length) + double(width)) * 2;
}
//=================================================================================
//
//=================================================================================
float Rectangle::calcArea()
{
	return length * width;
}
//=================================================================================
//
//=================================================================================
void Rectangle::setLength(float length)
{
	this->length = length;
}
//=================================================================================
//
//=================================================================================
void Rectangle::setWidth(float width)
{
	this->width = width;
}