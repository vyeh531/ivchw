// Triangle.cpp

#include "Triangle.h"

Triangle::Triangle()
{
	side1 = 0.;
	side2 = 0.;
	side3 = 0.;
}
//=================================================================================
//
//=================================================================================
Triangle::Triangle(float side1, float side2, float side3)
{
	this->side1 = side1;
	this->side2 = side2;
	this->side3 = side3;
}
//=================================================================================
//
//=================================================================================
Triangle::~Triangle() {}
//=================================================================================
//
//=================================================================================
double Triangle::calcPerimeter()
{
	return double(side1) + double(side2) + double(side3);
}
//=================================================================================
//
//=================================================================================
float Triangle::calcArea()
{
	float s = 0.0;
	float temp = 0.0;
	s = side1 + side2 + side3 / 2;
	temp = s * (s - side1) * (s - side2) * (s - side3);
	
	return sqrt(temp);
}
//=================================================================================
//
//=================================================================================
void Triangle::setSide1(float side1)
{
	this->side1 = side1;
}
//=================================================================================
//
//=================================================================================
void Triangle::setSide2(float side2)
{
	this->side2 = side2;
}
//=================================================================================
//
//=================================================================================
void Triangle::setSide3(float side3)
{
	this->side3 = side3;
}